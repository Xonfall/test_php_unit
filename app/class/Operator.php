class Operator {

    public static function add (int $a, int $b) {
        return $a + $b;
    }

    public static function sub (int $a, int $b) {
        return $a - $b;
    }

    public static function mul (int $a, int $b) {
        return $a * $b;
    }

    public static function div (int $a, int $b) {
        return $a / $b;
    }
}